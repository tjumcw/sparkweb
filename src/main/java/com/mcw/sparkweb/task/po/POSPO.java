package com.mcw.sparkweb.task.po;

public class POSPO extends BasePO{

    private String id;

    private String sofa;

    private String begin;

    private String end;

    private String posValue;

    private String coarseValue;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSofa() {
        return sofa;
    }

    public void setSofa(String sofa) {
        this.sofa = sofa;
    }

    public String getBegin() {
        return begin;
    }

    public void setBegin(String begin) {
        this.begin = begin;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }

    public String getPosValue() {
        return posValue;
    }

    public void setPosValue(String posValue) {
        this.posValue = posValue;
    }

    public String getCoarseValue() {
        return coarseValue;
    }

    public void setCoarseValue(String coarseValue) {
        this.coarseValue = coarseValue;
    }
}
